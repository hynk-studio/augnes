function readChangedFiles() {
  return Array.from(
    new Set([
      ...readGitOutput(["diff", "--name-only", mergeBaseRef()])
        .split("\n")
        .filter(Boolean),
      ...readGitOutput(["ls-files", "--others", "--exclude-standard"])
        .split("\n")
        .filter(Boolean),
    ]),
  ).sort();
}

function readGitOutput(args) {
  return execFileSync("git", args, { encoding: "utf8" }).trimEnd();
}

function mergeBaseRef() {
  if (cachedMergeBaseRef) {
    return cachedMergeBaseRef;
  }
  for (const candidate of [
    ["merge-base", "HEAD", "origin/main"],
    ["merge-base", "HEAD", "main"],
  ]) {
    try {
      cachedMergeBaseRef = readGitOutput(candidate);
      return cachedMergeBaseRef;
    } catch {
      // Try the next portable candidate.
    }
  }
  try {
    cachedMergeBaseRef = readGitOutput(["rev-parse", "HEAD^"]);
    return cachedMergeBaseRef;
  } catch (error) {
    throw new Error(
      `Unable to resolve merge base from origin/main, local main, or HEAD^: ${error}`,
    );
  }
}

function stripNonCode(source) {
  return source
    .replace(/\/\*[\s\S]*?\*\//g, "")
    .replace(/\/\/.*$/gm, "")
    .replace(/(["'`])(?:\\.|(?!\1)[\s\S])*\1/g, "\"\"");
}

function clone(value) {
  return JSON.parse(JSON.stringify(value));
}
